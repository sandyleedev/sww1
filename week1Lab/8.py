"""
8. Given that is_monday = True and is_sunny = False predict what each will
output:
    a. Print(is_monday and is_sunny)
    b. print(is_monday or is_sunny)
    c. print(not is_sunny)
"""

is_monday = True
is_sunny = False

print(is_monday and is_sunny)       # True and False -> False
print(is_monday or is_sunny)        # True or False -> True
print(not is_sunny)                 # not False -> True