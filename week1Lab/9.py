"""
9. Write Python code that compares two numbers x = 12 and y = 8.
Check and print whether:
    a. x is greater than y
    b. x is equal to y
    c. x is less than or equal to 25
"""

x = 12
y = 8

a = x > y       # 12 > 8 -> True
b = x == y      # False
c = x <= 25     # 12 <= 25 -> True


print(a)
print(b)
print(c)